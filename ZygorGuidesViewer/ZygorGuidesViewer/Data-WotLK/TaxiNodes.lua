-- Flight-master positions for the 3.3.5a world.  These are location records,
-- not a list of paths the player is assumed to know: Compat.Taxi filters this
-- table against the nodes captured from the client's taxi map/SavedVariables.
-- That makes a route suggest only flight paths that the character has learned.
local ZGV = ZygorGuidesViewer
local nodes = {}

local function add(map, name, x, y, faction, aliases)
  local key = (map .. ":" .. name .. ":" .. tostring(x) .. ":" .. tostring(y)):lower():gsub("[^%w]+", "-")
  nodes[#nodes + 1] = {
    key = key, mapKey = map .. "/0", name = name, title = name .. " Flight Master",
    x = tonumber(x) / 100, y = tonumber(y) / 100, faction = faction, aliases = aliases,
  }
end

